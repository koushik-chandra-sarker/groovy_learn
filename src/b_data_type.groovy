//Example of byte
byte b = 2
println("================Byte================")
println(b) //output: 2
println("Minimum value of byte: "+ Byte.MIN_VALUE) //output: Minimum value of byte: -128
println("Maximum value of byte: "+ Byte.MAX_VALUE) //output: Maximum value of byte: 127


//Example of short
short s = 3
println("================Short================")
println(s) //output: 3
println("Minimum value of short "+ Short.MIN_VALUE) //output: Minimum value of short -32768
println("Maximum value of short: "+ Short.MAX_VALUE) //output: Maximum value of short: 32767

//Example of int
int i  = 50
println("================Int================")
println(i) //output: 50
println("Minimum value of int: "+ Integer.MIN_VALUE)//output: Minimum value of int: -2147483648
println("Maximum value of int: "+ Integer.MAX_VALUE) //output: Maximum value of int: 2147483647

//Example of long
long l  = 5455
println("================Long================")
println(l) //output: 5455
println("Minimum value of int: "+ Long.MIN_VALUE)//output: Minimum value of long: -2147483648
println("Maximum value of int: "+ Long.MAX_VALUE) //output: Maximum value of long: 2147483647


//Example of float
float f  = 5.15
float f1  = 5.153215165465351
println("================Float================")
println(f) //output: 5.15
println(f1) //output: 5.153215
println("Minimum value of float: "+ Float.MIN_VALUE)//output: Minimum value of float: 1.4E-45
println("Maximum value of float: "+ Float.MAX_VALUE) //output: Maximum value of float: 3.4028235E38

//Example of double
double d  = 5.153215165465351
println("================Double================")
println(d) //output: 5.153215165465351
println("Minimum value of double: "+ Double.MIN_VALUE)//output: Minimum value of double: 4.9E-324
println("Maximum value of double: "+ Double.MAX_VALUE) //output: Maximum value of double: 1.7976931348623157E308

//Example of char
char c  = "a"
println("================Character================")
println(c) //output: a
println("Minimum value of char: "+ Character.MIN_VALUE) // see the console
println("Maximum value of char: "+ Character.MAX_VALUE) // see the console

//Example of boolean
/**
 * Boolean value either true nor false
 * */
boolean bool  = true
boolean bool1  = false
println("================Boolean================")
println(bool) //output: true
println(bool1) //output: false\


String st  = "Hello Groovy"
println("================String================")
println(st) //output: Hello Groovy




